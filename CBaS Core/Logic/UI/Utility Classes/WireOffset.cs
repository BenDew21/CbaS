namespace CBaSCore.Logic.UI.Utility_Classes
{
    public class WireOffset
    {
        private double _xOffset;
        private double _yOffset;

        public double XOffset
        {
            get
            {
                return _xOffset;
            }
            set
            {
                _xOffset = value;
            }
        }

        public double YOffset
        {
            get
            {
                return _yOffset;
            }
            set
            {
                _yOffset = value;
            }
        }
    }
}